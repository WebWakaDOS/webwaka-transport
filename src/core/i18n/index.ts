let currentLanguage: Language = (localStorage?.getItem('trn_language') as Language) ?? 'en';

export function t(key: string): string {
  return translations[currentLanguage]?.[key] ?? translations.en[key] ?? key;
}

export function setLanguage(lang: Language): void {
  currentLanguage = lang;
  try { localStorage?.setItem('trn_language', lang); } catch { /* SSR */ }
}

export function getLanguage(): Language {
  return currentLanguage;
}

export function getSupportedLanguages(): Array<{ code: Language; name: string }> {
  return [
    { code: 'en', name: 'English' },
    { code: 'yo', name: 'Yorùbá' },
    { code: 'ig', name: 'Igbo' },
    { code: 'ha', name: 'Hausa' },
  ];
}

/** Nigeria-First: format kobo integer as ₦ naira string */
export function formatKoboToNaira(kobo: number): string {
  const naira = kobo / 100;
  return `₦${naira.toLocaleString('en-NG', { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
}
